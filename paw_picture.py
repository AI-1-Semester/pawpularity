    self.Face = Face
    self.Near = Near
    self.Action = Action
    self.Accessory = Accessory
    self.Group = Group
    self.Collage = Collage
    self.Human = Human
    self.Occlusion = Occlusion
    self.Info = Info
    self.Blur = Blur